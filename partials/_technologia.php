                <h2 class="section-title">Langages et outils de développement</h2>

                <h3 class="section-subtitle">1 - Frontend</h3>
                <p>Le développement de l’interface utilisateur de MyGreen Campus repose sur des technologies web standards garantissant accessibilité, performance et compatibilité multiplateforme.</p>
                <p>Les langages et outils utilisés côté frontend sont :</p>
                <ul>
                    <li>HTML pour la structure des pages</li>
                    <li>CSS pour la mise en forme et le design responsive</li>
                    <li>JavaScript pour l’interactivité et la dynamique des contenus</li>
                    <li>Moteur de templates Twig (intégré à Symfony) pour la génération dynamique des vues</li>
                </ul>

                <h3 class="section-subtitle">2 - Backend</h3>
                <p>La partie serveur du projet est développée en PHP avec le framework Symfony, qui repose sur une architecture MVC (Modèle - Vue - Contrôleur). Cette structure permet une organisation claire du code et une séparation efficace des responsabilités.</p>
                <p>Le calcul de l’empreinte carbone repose soit sur :</p>
                <ul>
                    <li>Une API de calcul d’empreinte carbone</li>
                    <li>Ou une logique de calcul interne basée sur des données publiques fiables</li>
                </ul>

                <h3 class="section-subtitle">3 - Base de données</h3>
                <p>La gestion des données est assurée par une base de données relationnelle :</p>
                <ul>
                    <li>MySQL pour le stockage des informations utilisateurs et des résultats</li>
                    <li>Doctrine ORM pour la gestion des entités et la communication entre Symfony et la base de données</li>
                </ul>

                <h3 class="section-subtitle">4 - Outils de conception et gestion</h3>
                <p>Plusieurs outils ont été utilisés pour assurer la collaboration, l’organisation et la conception du projet :</p>
                <ul>
                    <li>Github pour le versionning du code, avec une branche principale et une branche dédiée à chaque membre AW du groupe</li>
                    <li>Microsoft Teams pour la communication et la gestion des tâches</li>
                    <li>Google Docs / Microsoft Word pour la rédaction des documents</li>
                    <li>Canva pour la conception graphique et les supports visuels</li>
                </ul>
            </div>
        </div>
    </div>
