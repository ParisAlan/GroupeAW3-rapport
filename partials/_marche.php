                <h2 class="section-title">Étude de marché</h2>

                <h3 class="section-subtitle"> 1 - Définition du marché</h3>
                <p>MyGreen Campus s'inscrit dans le marché des solutions numériques de sensibilisation à l'impact environnemental, plus précisément dans le segment des calculateurs d'empreinte carbone accessibles en ligne. Ce marché est en croissance, porté par l'augmentation de la prise de conscience écologique et par la digitalisation des usages.</p>
                <p>Le projet se positionne sur un segment spécifique : les étudiants universitaires et leur mode de vie particulier.</p>

                <h3 class="section-subtitle"> 2 - Analyse de la demande</h3>
                <p>La cible principale est constituée des étudiants âgés de 18 à 25 ans. Cette population est fortement connectée et utilise quotidiennement des outils numériques. Elle est également de plus en plus sensible aux enjeux climatiques, mais ne dispose pas toujours d'outils adaptés pour mesurer concrètement son impact carbone.</p>
                <p>Les besoins identifiés sont les suivants :</p>
                <ul>
                    <li>Comprendre son impact environnemental de manière simple et rapide</li>
                    <li>Recevoir des recommandations adaptées à la réalité étudiante (budget, logement, transport)</li>
                    <li>Être accompagné dans une démarche progressive et motivante</li>
                    <li>Disposer d'une interface intuitive et pédagogique</li>
                </ul>
                <p>Les cibles secondaires sont les établissements d'enseignement supérieur et les associations étudiantes. Ces acteurs peuvent rechercher des outils de sensibilisation à destination de leur communauté et des supports concrets pour valoriser leurs engagements environnementaux.</p>

                <h3 class="section-subtitle"> 3 - Analyse de l'offre</h3>
                <p>Plusieurs outils de calcul d'empreinte carbone existent déjà sur le marché, notamment des plateformes institutionnelles ou des applications privées. Ces solutions proposent généralement une estimation des émissions à partir des habitudes de consommation et de transport.</p>
                <p>Leurs principales forces résident dans la crédibilité scientifique et la reconnaissance institutionnelle. Cependant, elles restent majoritairement généralistes et ne prennent pas en compte les spécificités du mode de vie étudiant. Elles intègrent rarement une dimension campus ou une approche ludique favorisant l'engagement.</p>
                <p>MyGreen Campus se différencie par :</p>
                <ul>
                    <li>Un ciblage spécifique des étudiants</li>
                    <li>Une adaptation des recommandations au contexte universitaire</li>
                    <li>Une dimension de gamification avec défis et badges</li>
                    <li>Une approche pédagogique visant à rendre les données compréhensibles</li>
                </ul>

                <h3 class="section-subtitle"> 4 - Positionnement</h3>
                <p>Le projet ne cherche pas à concurrencer directement les outils scientifiques généralistes, mais à proposer une version adaptée, accessible et contextualisée pour les étudiants. Il se positionne comme un outil pédagogique et engageant, complémentaire aux solutions existantes.</p>

                <h3 class="section-subtitle"> 5 - Conclusion</h3>
                <p>Le marché des calculateurs d'empreinte carbone est déjà structuré, mais peu d'acteurs proposent une solution spécifiquement conçue pour les étudiants. MyGreen Campus répond à un besoin identifié d'adaptation, de simplification et de motivation, ce qui constitue un axe de différenciation pertinent dans le cadre d'un projet à vocation pédagogique et universitaire.</p>
