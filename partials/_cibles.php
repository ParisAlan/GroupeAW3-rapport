                <h2 class="section-title">Cibles du produit</h2>

                <h3 class="section-subtitle">1 - Nature du produit</h3>
                <p>MyGreen Campus est un outil logiciel proposé sous forme de service web. Il est accessible en ligne et permet aux utilisateurs d’évaluer et de mieux comprendre leur impact environnemental à travers une interface simple et pédagogique.</p>

                <h3 class="section-subtitle">2 - Cibles principales</h3>
                <p>La cible principale du produit est constituée des étudiants universitaires âgés de 18 à 25 ans.</p>
                <p>Le service s’adresse notamment aux :</p>
                <ul>
                    <li>Étudiants en résidence universitaire</li>
                    <li>Étudiants en colocation</li>
                    <li>Étudiants vivant au domicile parental</li>
                    <li>Étudiants sensibles à l’écologie</li>
                    <li>Étudiants souhaitant mieux comprendre leur impact environnemental, même sans connaissances préalables</li>
                </ul>

                <h3 class="section-subtitle">3 - Cibles secondaires</h3>
                <p>Le projet vise également des acteurs institutionnels et associatifs :</p>
                <ul>
                    <li>Les établissements d’enseignement supérieur, dans une logique d’outil pédagogique</li>
                    <li>Les associations étudiantes ou écologiques souhaitant sensibiliser les étudiants sur les campus</li>
                </ul>

                <h3 class="section-subtitle">4 - Positionnement auprès des cibles</h3>
                <p>MyGreen Campus se positionne comme un outil accessible, éducatif et adapté au mode de vie étudiant. Il facilite la compréhension des enjeux environnementaux et encourage une déaligner progressive d’amélioration des habitudes quotidiennes.</p>
